#include "main.h"

int			ft_atoi(char *str)
{
	int		n;

	n = 0;
	while (*str == '0')
	{
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		n = (*str - '0') + n * 10;
		str++;
	}
	return (n);
}
